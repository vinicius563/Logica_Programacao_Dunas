numero1 = 10
numero2 = 20

# Adição
soma = numero1 + numero2
print (soma)

# Subtração
subtracao = numero1 - numero2
print(subtracao)

# Multiplicação
multiplicacao = numero1 * numero2
print(multiplicacao)

# Divisão
divisao = numero1 / numero2
print(divisao)

# Potencia
potencia = numero1 ** numero2
print(potencia)

# Modulo
modulo = numero1 % numero2
print(modulo)

# Resto da divisao
restodadivisao = numero1 // numero2
print(restodadivisao)
